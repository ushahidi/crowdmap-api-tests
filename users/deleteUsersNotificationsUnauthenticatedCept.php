$I = new UserGuy($scenario);
$I->wantTo('test DELETE request against the /user/:user_id/notifications/:notification_id end-point without authentication');
$I->haveHttpHeader('Content-Type', 'text/html');
$I->haveHttpHeader('User-Agent', 'Api Test/0.1');

//test request to mark notifications as read
$I->sendDelete('/users/6657/notifications');
